function animateCounter(element, target, suffix) {
  const duration = 1100;
  const start = performance.now();

  const step = (timestamp) => {
    const progress = Math.min((timestamp - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    const value = Math.round(target * eased);
    element.textContent = `${value}${suffix}`;

    if (progress < 1) {
      requestAnimationFrame(step);
    }
  };

  requestAnimationFrame(step);
}

function initImpactCounters() {
  const counters = Array.from(document.querySelectorAll(".impact-counter"));
  if (!counters.length) {
    return;
  }

  counters.forEach((counter) => {
    counter.textContent = `0${counter.dataset.suffix || ""}`;
  });

  const run = () => {
    counters.forEach((counter) => {
      const target = Number(counter.dataset.countTo || "0");
      const suffix = counter.dataset.suffix || "";
      if (target > 0) {
        animateCounter(counter, target, suffix);
      }
    });
  };

  if (impactObserver) {
    impactObserver.disconnect();
  }

  if ("IntersectionObserver" in window) {
    impactObserver = new IntersectionObserver(
      (entries) => {
        if (entries.some((entry) => entry.isIntersecting)) {
          run();
          impactObserver.disconnect();
        }
      },
      { threshold: 0.4 }
    );
    impactObserver.observe(elements.impact);
  } else {
    run();
  }
}

function initRevealAnimations() {
  const revealElements = Array.from(document.querySelectorAll(".reveal"));
  revealElements.forEach((element, index) => {
    element.style.setProperty("--reveal-delay", `${Math.min(index * 45, 260)}ms`);
  });

  if (reducedMotionQuery.matches) {
    if (revealObserver) {
      revealObserver.disconnect();
    }
    revealElements.forEach((element) => element.classList.add("in-view"));
    return;
  }

  if (revealObserver) {
    revealObserver.disconnect();
  }

  if ("IntersectionObserver" in window) {
    revealObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("in-view");
            revealObserver.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.14, rootMargin: "0px 0px -4% 0px" }
    );

    revealElements.forEach((element) => revealObserver.observe(element));
  } else {
    revealElements.forEach((element) => element.classList.add("in-view"));
  }
}

